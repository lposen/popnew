				<h3>Beyond the Build</h3>
				<p>A classroom is, and always should be, much more than a building. At Pencils of Promise we believe that a classroom should be a place of high-quality learning that is open to every child, regardless of socio-economic situation, gender and ethnicity.</p>
				
				<h4>Teacher Training</h4>
				<p>Our teacher training program aims to work directly with teachers to support them in being active and engaging in the classroom. We believe a good teacher is key to learning and want to make sure our teachers have a confident handle on the curriculum to pass on to their students. The teacher training program was successfully piloted in Laos and will expend to Guatemala in 2013. </p>
				
				<p>Learn more: <a href="http://www.pencilsofpromise.org/blog/from-the-field/teacher-training-in-laos">http://www.pencilsofpromise.org/blog/from-the-field/teacher-training-in-laos</a>  </p>
				
				<h4> Scholarships</h4>
				<p>As a way to support access to our schools, students who show financial need and academic merit can be selected to receive a scholarship from us. Scholarship recipients must hit benchmarks of regular attendance and passing grades to maintain their scholarship funds. </p>
				<p>Learn more: <a href="http://www.pencilsofpromise.org/blog/from-the-field/scholarships-in-laos">http://www.pencilsofpromise.org/blog/from-the-field/scholarships-in-laos</a>
				
				<h4>Emergency Fund</h4>
				<p>An emergency fund is given to the Promise Committee in each village with the direction that is can be given to any family who has a primary-aged student in their household and is suffering a financial emergency such as sickness or an accident. Our vision for this fund is to have fewer students missing school due to family emergencies, as well as fostering increased trust between PoP and the Promise Committees.</p>
				<p>Learn more: <a href="http://www.pencilsofpromise.org/blog/from-the-field/emergency-funds">http://www.pencilsofpromise.org/blog/from-the-field/emergency-funds</a>
				
				
				<h4>S.H.I.N.E.</h4>
				<p>Supplemental lessons on Sanitation, Health, Identity, Nutrition, and Environment, also known as S.H.I.N.E, are designed to result in improved educational achievement by teaching students to lead healthier and more fulfilling lifestyles.</p>
				<p>Learn more: <a href="http://www.pencilsofpromise.org/blog/shine-program/meet-our-new-shine-teachers-in-pop-laos">http://www.pencilsofpromise.org/blog/shine-program/meet-our-new-shine-teachers-in-pop-laos</a>
				
