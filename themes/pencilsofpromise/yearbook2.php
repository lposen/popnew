			<h3>It started with a pencil</h3>
			<p>In 2008, Adam Braun was a young Brown grad and consultant at Bain & Company by trade, backpacker by passion. He developed a travel habit. Adam asked one child in each country what they would want if they could have anything in the world. One boy in South Asia said that his biggest wish was to have a pencil.</p>
			<p>The greatest minds all started with a pencil and paper. It’s how we learn to express ourselves and our dreams. A pencil is the first tool to pursue a better future.</p>
			<br/>
			<h3>Then 1 school</h3>
			<p>Adam put $25 into a bank account and threw himself a 25th birthday party with the goal of building one school. Together, Adam’s friends raised $25,000 which built Pha Theung, a two classroom pre-school in Laos. He used Facebook to share photos of the build process and, finally, of the pre-school students on their very first day in their new classrooms. His friends saw their dollars changing lives.</p>
			<br/>
			<h3>Then 100 schools</h3>
			<p>They wanted to build schools of their own. So did their families. So did the companies they worked for. Adam quit his job at Bain and started Pencils of Promise, uniting his business background with his passion for social change.</p>
			<p>Today, PoP works in Laos, Guatemala, Nicaragua and Ghana and has broken ground on over 115 pre and primary schools in remote and underserved regions of these countries. Each school was made possible by a person, a family or a company as idealistic and inspired as Adam as a young backpacker.</p>
