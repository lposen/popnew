				<img src="http://dbc468c6af3659a35fbc-f0981fd138ea8adbe310045bdc6ef280.r92.cf1.rackcdn.com/School%20(1).png" style="padding-top:80px;">
				<p style="padding-top:70px; width:350px; float:right;">A school is, and always should be, much more than a building. At Pencils of Promise we believe that a classroom should be a place of high-quality learning that is open to every child, regardless of socio-economic situation, gender and ethnicity.</p>
				</div>
				
				<div class="container">
				<h3>Teacher Training</h3>
				<div id="teachtraining"></div>
				<p style="padding-top:20px; width:300px; text-align:justify;">Our teacher training program aims to work directly with teachers to support them in being active and engaging in the classroom. We believe a good teacher is key to learning and want to make sure our teachers have a confident handle on the curriculum to pass on to their students. The teacher training program was successfully piloted in Laos and will expend to Guatemala in 2013. </p>
				</div>
				<div class="container">
				<h3> Scholarships</h3>
					<div id="scholarships"></div>
				<p style="width:270px; float:left; padding-top:15px;">As a way to support access to our schools, students who show financial need and academic merit can be selected to receive a scholarship from us. Scholarship recipients must hit benchmarks of regular attendance and passing grades to maintain their scholarship funds. </p>
				</div>
				<div class="container">
				<h3>Emergency Fund</h3>
				<div id="emerfund"></div>
				<p style="width:300px; text-align:justify;">An <b>emergency fund</b> is given to the Promise Committee in each village with the direction that is can be given to any family who has a primary-aged student in their household and is suffering a financial emergency such as sickness or an accident. Our vision for this fund is to have fewer students missing school due to family emergencies, as well as fostering increased trust between PoP and the Promise Committees.</p>
				</div>
				<div class="container">
				<h3>S.H.I.N.E.</h3>
				<div id="shine"></div>
				<p style="float:left; width:280px;">Supplemental lessons on Sanitation, Health, Identity, Nutrition, and Environment, also known as S.H.I.N.E, are designed to result in improved educational achievement by teaching students to lead healthier and more fulfilling lifestyles.</p>
				</div>
				
				<div class="container">
					<h3>Impact Metrics</h3>
					<p><b>Monitoring and evaluation</b> is key to our process because it allows our teams to measure the successes and identify the opportunities for improvement in our work. When you know what works and what doesn’t, you know how to address the issue most effectively. </p>
					<p style="font-weight:bold; text-transform:uppercase; line-height: 15px;width: 31%; font-size: 11px;margin-left: auto; margin-right: auto;">PLEASE MEET NOVY AND ANASTACIA, TWO MEMBERS OF OUR WORLD-WIDE MONITORING AND EVALUATION TEAM.</p>
				<div id="bbmeet" style="float:left;">
					<div class="novy"></div>
					<h5>Novy</h5>
					<p>He has identified the metrics by which we hold ourselves accountable. </p>
				
					<p>Metrics are what we measure. This can be anything from test scores to attendance and dropout rates. It’s important to not only measure a variety of things - but the right things because it allows us to see the big picture. </p>
